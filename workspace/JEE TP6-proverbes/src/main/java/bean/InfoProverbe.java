package bean;

public class InfoProverbe {
	private String nom;
	private String proverbe;
	private int numProverbe;
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getProverbe() {
		return proverbe;
	}
	public void setProverbe(String proverbe) {
		this.proverbe = proverbe;
	}
	public int getNumProverbe() {
		return numProverbe;
	}
	public void setNumProverbe(int numProverbe) {
		this.numProverbe = numProverbe;
	}
}
